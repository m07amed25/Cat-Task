'''
A6:
    The difference between the WHERE and HAVING clauses in SQL comes
    in when and how they are used to filter data in a query.
    ==========
    WHERE: Filters rows before any grouping or aggregation.
    HAVING: Filters groups after aggregation has been performed.
'''

'''
A7:
    Delete: Removes specific rows from a table based on a condition.
    TRUNCATE: Removes all rows from a table.
'''

'''
A8:
    - INNER JOIN: Returns rows where there is a match in both tables.
    - LEFT JOIN: Returns all rows from the left table and the matching rows from the right table.
    - RIGHT JOIN: Returns all rows from the right table and the matching rows from the left table.
    - FULL JOIN: Combines the results of both LEFT JOIN and RIGHT JOIN.
'''